#include <stdio.h>
#include <stdlib.h>
/**
 * main - main block
 * Description: program that will print all possible
 * different combinations of digits
 * Return: 0
 */
int main(void)
{
	int c;
	int d = 0;

	while (d < 10)
	{
		c = 0;
		while (c < 10)
		{
			if (d != c && d < c)
			{
				putchar(48 + d);
				putchar(48 + c);

				if (c + d != 17)
				{
					putchar(',');
					putchar(' ');
				}
			}

			c++;
		}
		d++;
	}
	putchar('\n');
	return (0);
}
